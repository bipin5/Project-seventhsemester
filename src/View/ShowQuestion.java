    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        if (questionTable.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(null, "Select any Row");
            return;
        }

        int row = questionTable.getSelectedRow();
        int id = (int) questionTable.getModel().getValueAt(row, 0);

        QuestionPaperDao qdao = new QuestionPaperDaoimpl();

        if (qdao.DeleteQuestion(id)) {
            JOptionPane.showMessageDialog(null, "deleted success");
            fetch();
        } else {
            JOptionPane.showMessageDialog(null, "failed");        
        
        }
       
    }//GEN-LAST:event_jButtonDeleteActionPerformed

