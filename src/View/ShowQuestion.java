    public void fetch(){

        QuestionPaperDao qdao= new QuestionPaperDaoimpl();
        List<Question> qlist = qdao.getallQuestion();
        
        DefaultTableModel model= (DefaultTableModel)questionTable.getModel();
        model.setRowCount(0);
        
        for(Question q:qlist){
            model.addRow(new Object[]{q.getId(),q.getQuestion(),q.getDifficulty(),q.getSubject_name(),q.getSubject_code(),q.getSemester(),q.getSubject_set()});
        }
    }  

